# Matrix is a way to describe 2D lists or multi-dimensional lists
# Matrix is an array with another array inside of it.
# This is a 2D array where we have a main array and sub arrays inside of it (3 arrays to be exact).
two_d_matrix = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
]
print(two_d_matrix[2][2])

three_d_matrix = [
                     [1, 2, 3],
                     [4, 5, 6],
                     [7, 8, 9]
                 ], \
                 [
                     [9, 8, 7],
                     [6, 5, 4],
                     [3, 2, 1]
                 ]
print(three_d_matrix[1][1][1])
