name = 'Nithin'
age = 30

# Python 3 onwards
print(f'Hi {name}, you are {age} years old!')

# Prior to Python 3
print('Hi {}, you are {} years old!'.format('Nitin', 31))
print('You are {1} years old, {0}!'.format(name, age))
print('Hi {new_name}, you both are {new_age} years old!'.format(new_name='Adelina and Ariana', new_age=2))
