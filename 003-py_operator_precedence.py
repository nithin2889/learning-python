# operator precedence
# ()
# **
# / and *
# + and -
print("---------------")
print(20 - 3 * 4)          # 8
print((20 - 3) + 2 ** 2)   # 21
print((18 / 3) + 2 * 2)    # 10.0

print("---------------")
print((5 + 4) * 10 / 2)    # 9 * 5 = 45.0
print(((5 + 4) * 10) / 2)  # 90 / 2 = 45.0
print((5 + 4) * (10 / 2))  # 9 * 5 = 45.0
print(5 + (4 * 10) / 2)    # 5 + 40 / 2 = 5 + 20 = 25.0
print(5 + 4 * 10 // 2)     # 5 + 40 // 2 = 5 + 20 = 25
