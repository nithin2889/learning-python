# length of a string
greeting = 'helllooooo'
print(greeting[0:len(greeting)])

# upper
quote = 'to be or not to be'
print(quote.upper())

# capitalize
print(quote.capitalize())

# find (finds the first occurance)
print(quote.find('be', 5))

# replace old string with a new one
print(quote.replace('be', 'me'))
